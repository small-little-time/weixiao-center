package com.zjw.graduation.config.dto;

import com.zjw.graduation.entity.adm.AdmAdmin;
import com.zjw.graduation.entity.adm.AdmPermission;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;
import java.util.stream.Collectors;

/**
 * SpringSecurity需要的用户详情
 * Created by macro on 2018/4/26.
 */
public class AdminUserDetails implements UserDetails {
    private AdmAdmin admAdmin;
    private List<AdmPermission> permissionList;
    public AdminUserDetails(AdmAdmin admAdmin, List<AdmPermission> permissionList) {
        this.admAdmin = admAdmin;
        this.permissionList = permissionList;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        //返回当前用户的权限
        return permissionList.stream()
                .filter(permission -> permission.getValue()!=null)
                .map(permission ->new SimpleGrantedAuthority(permission.getValue()))
                .collect(Collectors.toList());
    }

    @Override
    public String getPassword() {
        return admAdmin.getPassword();
    }

    @Override
    public String getUsername() {
        return admAdmin.getUsername();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return admAdmin.getStatus().equals(1);
    }
}
